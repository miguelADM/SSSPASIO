                            estar
                            en el mismo eje del cuerpo, no debes usar almohadas ya que hace que nos inclinemos o nos
                            lleva
                            muy atr&aacute;s, eso va a producir alteraciones en la columna y contracturas
                            musculares.</big>
                    </p>

                    <p style="text-align:justify"><big>&bull; Si estamos sentados, la espalda debe estar apoyada en el
                            respaldar del asiento.</big>
                    </p>

