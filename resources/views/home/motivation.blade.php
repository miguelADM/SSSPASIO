                </div>

                <div class="main__article">
                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Mientras que es recomendable que las
                                    personas adultas participen en al menos 30 minutos de ejercicio moderado, cinco o
                                    m&aacute;s d&iacute;as por semana, la mayor&iacute;a de las personas se quedan
                                    cortas.</cite></span></big></h3>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Todo se trata de lo que funcione
                                    mejor para usted, ya que no todos los programas de ejercicios funcionan bien para
                                    todas las personas. Deben ser individualizados y basados en las necesidades de cada
                                    uno.</cite></span></big></h3>

                    <h3>&nbsp;</h3>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Comenzar no es tan dif&iacute;cil
                                    como parece, especialmente si usted tiene una actitud positiva hacia la actividad
                                    f&iacute;sica. y para no decaer te ofrecemos&nbsp;estos
                                    consejos:</cite></span></big></h3>

                    <h3 style="text-align:justify">&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><strong><em>Encuentre una
                                                    actividad que le guste
                                                    hacer.&nbsp;</em></strong></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><span dir="ltr"><cite><big>Si su intenci&oacute;n es la de ir al
                                    gimnasio &nbsp;pero odia el que siempre est&eacute; lleno y le es desagradable que
                                    le critiquen por la forma en que hace el ejercicio, entonces no va a lograr mantener
                                    esa intenci&oacute;n. No se supone que esto sea una soluci&oacute;n r&aacute;pida,
                                    se supone que sea un cambio en su estilo de vida. Busque actividades que usted
                                    querr&aacute; continuar haciendo y en un ambiente que le sea agradable
                                    hacerlo.</big></cite></span></h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><em><strong>Divida sus
                                                    sesiones de actividad.</strong></em></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Mientras que la dosis sugerida de
                                    actividad es un m&iacute;nimo de 30 minutos por d&iacute;a, usted no tiene que
                                    hacerlo todo a la misma vez. Si es necesario, puede dividir sus actividades en
                                    sesiones m&aacute;s manejables de 10 minutos, tres veces al
                                    d&iacute;a.</cite></span></big></h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><em><strong>Var&iacute;e su
                                                    rutina</strong>.</em></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>&iquest;Le gusta caminar?
                                    &iexcl;Fant&aacute;stico! Pero nadar tambi&eacute;n puede ser divertido y
                                    refrescante, y bailar alrededor de su sala puede ser muy entretenido. Pruebe cosas
                                    distintas. As&iacute; ejercitar&aacute; las distintas partes de su cuerpo y
                                    tendr&aacute; menos probabilidades de aburrirse.</cite></span></big></h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><em><strong>P&oacute;ngase
                                                    metas realistas.&nbsp;</strong></em></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:center"><big><span dir="ltr"><cite>Comience despacio y aumente
                                    gradualmente. Una caminata o un paseo en bicicleta de 10 minutos puede ser todo lo
                                    que usted pueda aguantar al principio. Pero cuando eso comience a sentirse
                                    m&aacute;s f&aacute;cil o de rutina, aum&eacute;ntelo un poco al a&ntilde;adir
                                    m&aacute;s tiempo o m&aacute;s intensidad.</cite></span></big></h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><em><strong>No se enfoque
                                                    solamente en las actividades
                                                    cardiovasculares.&nbsp;</strong></em></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Incluya los distintos componentes de
                                    la actividad f&iacute;sica tales como los ejercicios de fortaleza, flexibilidad y
                                    equilibrio. Todos son importantes para su salud a largo plazo.</cite></span></big>
                    </h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><em><strong>No se comprare con
                                                    los dem&aacute;s.</strong>&nbsp;</em></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Su vecino podr&aacute; correr
                                    maratones, mientras que usted a penas puede darle la vuelta a la manzana. Pero eso
                                    no importa. Se trata de usted, no de c&oacute;mo los dem&aacute;s pueden percibirle
                                    o juzgarle. No se desanime sin tan siquiera haber comenzado.</cite></span></big>
                    </h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><em><strong>Aumente la
                                                    actividad conscientemente.</strong>&nbsp;</em></cite></span></big>
                            </h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Usted puede a&ntilde;adir m&aacute;s
                                    actividad aqu&iacute; y all&aacute; sin darse cuenta. P&aacute;rese en una pierna
                                    mientras se lava los dientes, traiga sus compras del carro una bolsa a la vez,
                                    est&iacute;rese o brinque durante los comerciales de televisi&oacute;n. Cuando
                                    estacione su carro, no busque el espacio m&aacute;s cercano a la entrada. En vez,
                                    tome una caminata por el estacionamiento hasta su destino. Todo eso se
                                    suma.</cite></span></big></h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><strong><em>P&oacute;ngase
                                                    retos</em></strong>&nbsp;</cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Una vez que comience a moverse, le
                                    sorprender&aacute; cu&aacute;n r&aacute;pido progresa. Pruebe nuevas actividades o
                                    aumente su rutina. Para obtener el mayor beneficio de su actividad f&iacute;sica,
                                    haga el esfuerzo por llegar al nivel.</cite></span></big></h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><em><strong>Siga su
                                                    progreso</strong></em></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>No importa cuando usted empiece. Lo
                                    que importa es cu&aacute;n lejos progrese. Escriba la cantidad de minutos de
                                    actividad, cuan lejos lleg&oacute; o cu&aacute;nto pudo lograr hacer. O use un
                                    pod&oacute;metro todos los d&iacute;as y vea como aumentan sus pasos. Celebre su
                                    compromiso continuo y cualquier mejor&iacute;a. &iexcl;Claro que debe sentirse
                                    orgulloso!</cite></span></big></h3>

                    <h3>&nbsp;</h3>

                    <ul>
                        <li>
                            <h3 style="text-align:center"><big><span dir="ltr"><cite><strong>Haga ejercicios con un
                                                amigo &ndash; quiz&aacute;s.&nbsp;</strong></cite></span></big></h3>
                        </li>
                    </ul>

                    <h3 style="text-align:justify"><big><span dir="ltr"><cite>Algunas personas disfrutan de hacer
                                    ejercicios con un compa&ntilde;ero y hasta unirse a un grupo. Pero no deje que la
                                    participaci&oacute;n de los dem&aacute;s le afecte la suya. Siga su jornada, aunque
                                    su compa&ntilde;ero no pueda &ndash; o no lo haga. Santiago-Charles explica,
                                    &ldquo;Es importante que la gente se apropie de sus metas de actividad y diga,
                                    &lsquo;Estoy haci&eacute;ndolo por mi&rsquo; &ldquo;.</cite></span></big></h3>

